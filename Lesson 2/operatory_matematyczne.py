x = 10
y = 5

print(x + y) # -> 15 dodawanie
print(x - y) # -> 5 odejmowanie
print(x * y) # -> 50 mnozenie
print(x / y) # -> 2.0 dzielenie - bo kazdy wynik dzielenia jest liczba necalkowita
print(x // y) # -> 2 dzielenie calkowite - czyli nie ma 2.0 tylko jest po prostu 2
print(10.5 // y) # -> 2.0 bo jedna z liczb jest liczba nie calkowita
print(x ** y) # -> 100000 potegowanie
print(x ** 0.5) # -> 3.16 pierwiastkowanie w tym przypadku pierwiastek z 10
print(x % 3) # -> 1 dzielenie z reszta